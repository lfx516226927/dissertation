function[F]=rightterm(N)
kap=0.3;
E=1;
L=1;
h=L/N;
%f01
k=-E*kap/(1-kap^2)-0.5*E/(1+kap);
r=k*h^2*ones(N,1);
r(N,1)=r(N,1)/2;
b=ones(N,1);
b(N,1)=b(N,1)/2;
r=sparse(r);
b=sparse(b);
a=sparse(1,1,1,3,1);
f01=kron(a,kron(b,r));
%f02
k=-E*kap/(1-kap^2)-0.5*E/(1+kap)-3/8;
r=k*h^2*ones(N,1);
r(N,1)=r(N,1)/2;
b=ones(N,1);
b(N,1)=b(N,1)/2;
r=sparse(r);
b=sparse(b);
a=sparse(2,1,1,3,1);
f02=kron(a,kron(b,r));
%q0
k=3/8;
r=k*h^2*ones(N,1);
r(N,1)=r(N,1)/2;
b=ones(N,1);
b(N,1)=b(N,1)/2;
r=sparse(r);
b=sparse(b);
a=sparse(3,1,1,3,1);
q0=kron(a,kron(b,r));
%f21
c=0.5*E/(1+kap)+1/8;
d=0.5*E/(1+kap);
k=E*kap/(1-kap^2)+E/(1+kap)+1/4;
m=E*kap/(1-kap^2);
b=k*h^2*linspace(1,N-1,N-1)+m*h;
b=b';
r=zeros(N,1);
b=sparse(b);
r=sparse(r);
r(N,1)=1;
f21=kron(b,r);
r=c*h^2*linspace(1,N,N)+d*h;
r=r';
r(N,1)=k*(N*h^2/2-h^2/6)+m*h/2+c*(N*h^2/2-h^2/6)+d*h/2;
r=sparse(r);
f21=[f21;r];
f21=kron(sparse(1,1,1,3,1),f21);
%f22
c=E*kap/(1-kap^2)+E/(1+kap);
d=E*kap/(1-kap^2)+1/4;
k=0.5*E/(1+kap);
m=0.5*E/(1+kap)+1/8;
b=k*h^2*linspace(1,N-1,N-1)+m*h;
b=b';
r=zeros(N,1);
b=sparse(b);
r=sparse(r);
r(N,1)=1;
f22=kron(b,r);
r=c*h^2*linspace(1,N,N)+d*h;
r=r';
r(N,1)=k*(N*h^2/2-h^2/6)+m*h/2+c*(N*h^2/2-h^2/6)+d*h/2;
r=sparse(r);
f22=[f22;r];
f22=kron(sparse(2,1,1,3,1),f22);
%q2
c=1/8-5;
d=1/8;
k=1/4-5;
m=1/4;
b=k*h^2*linspace(1,N-1,N-1)+m*h;
b=b';
r=zeros(N,1);
b=sparse(b);
r=sparse(r);
r(N,1)=1;
q2=kron(b,r);
r=c*h^2*linspace(1,N,N)+d*h;
r=r';
r(N,1)=k*(N*h^2/2-h^2/6)+m*h/2+c*(N*h^2/2-h^2/6)+d*h/2;
r=sparse(r);
q2=[q2;r];
q2=kron(sparse(3,1,1,3,1),q2);
F=f01+f02+f21+f22+q0-q2;
end